# Дополнительные операции: степень, остаток от деления и целочисленное деление
# степень
print(2 ** 0)
print(2 ** 1)
print(2 ** 2)
print(2 ** 3)
print(2 ** (-1))
print(2 ** 2 ** 3) # 2 ** (2 ** 3) = 2 ** 8

# Целочисленное деление
print(10 // 3)
print(-10 // 3)
print(10 // 4)
print(10 // 5)
print(10 // 6)
print(10 // 12)

# Деление с остатком
print(10 % 3)
print(10 % 4)
print(10 % 5)
print(10 % 6)
print(10 % 12)
print(10 % 20)
