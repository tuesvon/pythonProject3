# Переменная цикла

for i in range(10):
    print(i, "-- Hello!") # Начинается с 0
print("____")

for i in range(10):
    print(i + 1, "-- Hello!") # начинается с 1
print("____")

for number in range(10):
    print(number)
print("____")

for _ in range(5):
    print('Python - awesome!')
print("____")